#include "Blink.h"

/**********************************************************************************************************************
 *  Main Function
 *********************************************************************************************************************/
void blink();


int main(){

	blink_Init(3, 3, blink); // on time, off time, call-back function

	blink_Start();
	
	while(1){}
}

void blink(){
	
	if (Gpio_ReadPin(Pin_F2) == Gpio_LOW){
		Gpio_FlipPin(Pin_F2);
		Gpt_StartTimer(GPT_Timer1, on_Time);
	}
	else{
		Gpio_FlipPin(Pin_F2);
		Gpt_StartTimer(GPT_Timer1, off_Time);
	}
}


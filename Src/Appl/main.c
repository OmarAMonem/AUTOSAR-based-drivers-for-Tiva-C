/**********************************************************************************************************************
 *  INCLUDES
 *********************************************************************************************************************/
#include "Blink.h"

/**********************************************************************************************************************
 *  Main Function
 *********************************************************************************************************************/
void blink();

uint8 onTime = 3;
uint8 offTime = 1;

int main(){

	blink_Init(onTime, offTime, blink);

	blink_Start();
	
	while(1){}
}

void blink(){
	
	if (Gpio_ReadPin(Pin_F2) == Gpio_LOW){
		Gpio_FlipPin(Pin_F2);
		Gpt_StartTimer(GPT_Timer1, onTime);
	}
	else{
		Gpio_FlipPin(Pin_F2);
		Gpt_StartTimer(GPT_Timer1, offTime);
	}
}

/**********************************************************************************************************************
 *  END OF FILE: main.c
 *********************************************************************************************************************/
